/**
 * Coordinate location object.
 */
export interface Location {
  /**
   * X Coordinate.
   */
  x: number
  /**
   * Y Coordinate.
   */
  y: number
  /**
   * Z Coordinate.
   */
  z: number
}

/**
 * Rotation object.
 */
export interface Rotation {
  /**
   * X Coordinate.
   */
  x: number
  /**
   * Y Coordinate.
   */
  y: number
}
